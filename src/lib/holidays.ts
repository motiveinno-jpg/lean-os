// "YYYY-MM-DD" → 공휴일 이름(없으면 null)
export function getHoliday(ymd?: string | null): string | null {
  if (!ymd) return null;
  const m = /^(\d{4})-(\d{2})-(\d{2})/.exec(ymd);
  if (!m) return null;
  const year = +m[1];
  const md = `${m[2]}-${m[3]}`;
  const table = HOLIDAYS_BY_YEAR[year];
  if (table) return table[md] ?? null;
  return FIXED[md] ?? null; // 표 밖 연도: 양력 고정 공휴일만
}
