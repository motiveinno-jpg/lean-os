/** 현재 경로에 해당하는 메뉴 도움말을 최장 프리픽스로 찾는다. 없으면 null. */
export function findMenuGuide(pathname: string): MenuGuide | null {
  if (!pathname) return null;
  const path = pathname.split("?")[0];
  let best: MenuGuide | null = null;
  for (const g of MENU_GUIDES) {
    if (path === g.match || path.startsWith(g.match + "/")) {
      if (!best || g.match.length > best.match.length) best = g;
    }
  }
  return best;
}
